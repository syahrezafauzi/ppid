                        <b>2. Strategi dan Metode Pengawasan</b><br/><br/>
                            <b>a. Audit Berkala:</b> Melakukan audit rutin terhadap pelaksanaan kebijakan informasi publik untuk memastikan kepatuhan terhadap peraturan yang berlaku.<br/>
                            <b>b. Pengawasan Internal:</b> Membentuk tim pengawasan yang terdiri dari anggota internal untuk memantau dan mengevaluasi kinerja PPID secara berkala.<br/>
                            <b>c. Sistem Pelaporan:</b> Mengimplementasikan sistem pelaporan yang memudahkan masyarakat untuk melaporkan ketidakpuasan atau masalah yang dihadapi dalam mengakses informasi publik.
                        <br/><br/>
                        <b>3. Strategi dan Metode Monitoring</b><br/><br/>
                            <b>a. Sistem Informasi Manajemen:</b> Mengembangkan sistem informasi manajemen untuk memantau alur permohonan informasi publik dan menilai kinerja petugas.<br/>
                            <b>b. <i>Feedback</i> dari Masyarakat:</b> Mengumpulkan umpan balik dari masyarakat untuk mendapatkan insight mengenai pengalaman mereka dalam mengakses informasi.<br/>
                            <b>c. Laporan Berkala:</b> Membuat laporan berkala mengenai aktivitas PPID dan analisis tren permohonan informasi untuk mengidentifikasi area yang perlu diperbaiki.
