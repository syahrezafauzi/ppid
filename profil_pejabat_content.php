                  <p>
                      Selain itu, Kementerian Agama juga membentuk PPID Unit pada setiap perguruan tinggi agama negeri, yaitu Universitas Islam Negeri (UIN), Universitas Hindu Negeri (UHN), Institut Agama Islam Negeri (IAIN), 
                      Institut Hindu Dharma Negeri (IHDN), Institut Agama Kristen Negeri (IAKN), Sekolah Tinggi Agama Islam Negeri (STAIN), Sekolah Tinggi Agama Kristen Negeri (STAKN), Sekolah Tinggi Agama Hindu Negeri (STAHN) 
                      dan Sekolah Tinggi Agama Buddha Negeri (STABN).<br/><br/>

                      Pada tingkat Kantor Wilayah Kementerian Agama Provinsi dibentuk PPID, dimana Kepala Bagian Tata Usaha dan Humas sebagai PPID dan Kepala Kantor Wilayah sebagai Atasan PPID. 
                      Sedangkan di tingkat Kantor Kementerian Agama Kabupaten/Kota, PPID berada di Kepala Sub Bagian Tata Usaha dan Kepala Kantor sebagai Atasan PPID Unit.<br/><br/>

                      Pada Satuan Kerja Balai Penelitian dan Pengembangan/Balai Pendidikan dan Pelatihan Kementerian Agama dibentuk PPID, di mana Kepala Subbagian Tata Usaha sebagai PPID dan Kepala Balai Penelitian dan 
                      Pengembangan/Balai Pendidikan dan Pelatihan Kementerian Agama sebagai Atasan PPID Unit.<br/><br/>
