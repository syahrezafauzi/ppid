<p>Tugas, fungsi, dan wewenang PPID Kementerian Agama, diantaranya:<br/></p>
<h3>Tugas</h3>
<p>
1. menyediakan dan mengamankan Informasi Publik;<br/>
2. memberikan pelayanan Informasi Publik yang cepat, tepat, dan sederhana;<br/>
3. menyusun standar operasional prosedur pelaksanaan tugas dan kewenangan PPID Kementerian Agama dalam rangka penyebarluasan Informasi Publik;<br/>
4. menetapkan Daftar Informasi Publik dalam bentuk keputusan PPID Kementerian Agama mengenai Daftar Informasi Publik Kementerian Agama, paling lambat akhir bulan Januari tahun berjalan;<br/>
5. melaksanakan Pengklasifikasian Informasi Publik atau perubahannya dengan persetujuan Atasan PPID Kementerian Agama dalam bentuk keputusan PPID Kementerian Agama mengenai klasifikasi informasi Kementerian Agama;<br/>
6. menetapkan Informasi Publik yang Dikecualikan sebagai Informasi Publik yang dapat diakses dalam hal:
<ul>
    <li>
       telah dinyatakan terbuka bagi masyarakat berdasarkan mekanisme keberatan; 
    </li>
    <li>
        telah dinyatakan terbuka bagi masyarakat berdasarkan putusan sidang ajudikasi, putusan pengadilan, serta putusan Mahkamah Agung;
    </li>
    <li>
        telah habis jangka waktu pengecualiannya; dan/atau
    </li>
    <li>
        ditentukan oleh peraturan perundang-undangan;
    </li>
</ul>
7. menetapkan pertimbangan tertulis atas setiap kebijakan yang diambil guna memenuhi hak setiap orang atas Informasi Publik<br/>
8. mengoordinasikan:<br/>
<ul>
    <li>
        pengumpulan seluruh Informasi Publik yang meliputi: informasi yang wajib disediakan dan diumumkan secara berkala, informasi yang wajib diumumkan secara serta merta; dan informasi yang wajib tersedia setiap saat;
    </li>
    <li>
        pengumpulan Informasi Publik yang Dikecualikan;
    </li>
    <li>
        pengumuman Informasi Publik melalui media yang secara efektif dan efisien dapat menjangkau seluruh pemangku kepentingan;
    </li>
    <li>
        penyampaian Informasi Publik dalam bahasa Indonesia yang baik, benar, dan mudah dipahami;
    </li>
    <li>
        pemenuhan permohonan Informasi Publik yang dapat diakses oleh publik;
    </li>
    <li>
        pengklasifikasian Informasi Publik dan/ atau pengubahan Pengklasifikasian Informasi Publik;
    </li>
    <li>
        permohonan keberatan di proses berdasarkan prosedur;
    </li>
    <li>
        proses layanan Informasi Publik di Kementerian Agama berjalan dengan baik;
    </li>
</ul>
9. melakukan Uji Konsekuensi bersama dengan PPID Unit eselon I terhadap Informasi Publik yang tidak dapat diakses oleh masyarakat sebagaimana dimaksud dalam peraturan perundang-undangan mengenai keterbukaan Informasi Publik;<br/>
10. memberikan alasan tertulis atas pengecualian Informasi Publik, dalam hal permohonan Informasi Publik ditolak;<br/>
11. melakukan penghitaman atau pengaburan materi Informasi Publik yang Dikecualikan beserta alasannya;<br/>
12. menetapkan dan menugaskan petugas layanan informasi untuk membantu pelaksanaan tugas PPID Kementerian Agama;<br/>
13. melakukan pengembangan kompetensi petugas layanan informasi guna meningkatkan kualitas layanan informasi publik;<br/>
14. menggunakan Sistem Informasi PPID dalam pengelolaan layanan Informasi Publik;<br/>
15. menyediakan Informasi Publik yang mutakhir pada portal Kementerian Agama dan Sistem Informasi PPID;<br/>
16. memelihara dan/atau memutakhirkan informasi pada portal Kementerian Agama dan Sistem Informasi PPID sekurang-kurangnya 1 (satu) kali dalam 1 (satu) bulan;<br/>
17. melakukan koordinasi, harmonisasi, dan fasilitasi Perangkat PPID;<br/>
18. menyediakan ruangan dan/atau meja layanan Informasi Publik;<br/>
19. membuat dan menyampaikan Laporan Empat Bulanan Layanan Informasi Publik kepada Atasan PPID Kementerian Agama; dan<br/>
20. membuat dan mengumumkan Laporan Tahunan layanan Informasi Publik serta menyampaikan salinannya kepada Komisi Informasi Pusat.
</p>
<h3>Fungsi</h3>
<p>
    Pembinaan dan pengelolaan Pejabat Pengelola Informasi dan Dokumentasi di lingkungan Kementerian Agama Pusat dan Daerah.
</p>
<h3>Wewenang</h3>
<p>
    1. menetapkan Panitia Pengelola dan Pelayanan Informasi PPID Kementerian Agama;<br/>
    2. memutuskan suatu informasi dapat diakses atau tidak dapat diakses oleh masyarakat berdasarkan Uji Konsekuensi bersama dengan PPID Unit eselon I Pusat;<br/>
    3. menolak permohonan Informasi Publik secara tertulis apabila Informasi Publik yang dimohonkan termasuk Informasi Publik yang Dikecualikan dengan disertai alasan serta 
    pemberitahuan tentang hak dan tata cara bagi Pemohon untuk mengajukan keberatan atas penolakan tersebut;<br/>
    4. menghadiri rapat pembahasan terkait PPID di tingkat kementerian/lembaga;<br/>
    5. meminta informasi kepada PPID Unit pemilik informasi dalam hal Informasi Publik yang dimohonkan oleh Pemohon tidak dikuasai oleh PPID Kementerian Agama namun dikuasai oleh PPID Unit;<br/>
    6. melakukan koordinasi dengan PPID Unit terkait dalam menyelesaikan keberatan;<br/>
    7. melakukan pendampingan dan koordinasi dengan PPID Unit, unit teknis, dan/ atau unit yang memiliki tugas dan fungsi memberikan bantuan hukum, pendapat hukum, dan pertimbangan hukum yang berkaitan dengan tugas Kementerian Agama;<br/>
    8. mengusulkan kepada Atasan PPID Kementerian Agama untuk melaporkan dan/ atau mengajukan gugatan atas putusan Komisi Informasi ke lembaga peradilan;<br/>
    9. melakukan koordinasi dengan PPID Unit eselon I Pusat dalam penyediaan Informasi Publik yang mutakhir pada portal Kementerian Agama dan situs selain portal Kementerian Agama, dan/atau Sistem Informasi PPID;<br/>
    10. melaporkan ketidaksesuaian proses sidang Sengketa Informasi Publik ke Sekretariat Komisi Informasi atas persetujuan Atasan PPID Kementerian Agama; dan<br/>
    11. melakukan sosialisasi untuk meningkatkan pemahaman atas implementasi keterbukaan Informasi Publik di Kementerian Agama.<br/>
    12. melakukan pembinaan PPID Unit Kementerian Agama Pusat dan Daerah.<br/>
    13. melakukan monitoring terhadap pelaksanaan PPID Unit Kementerian Agama Pusat dan Daerah.<br/>
</p>


